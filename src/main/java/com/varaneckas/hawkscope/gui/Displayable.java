package com.varaneckas.hawkscope.gui;

/**
 * Displayable GUI object interface
 * 
 * @author Tomas Varaneckas
 * @version $Id$
 */
public interface Displayable {

    /**
     * Displays object
     */
    abstract public void showObject();
    
    /**
     * Hides object
     */
    abstract public void hideObject();
    
}
